package com.famsa.exceptions;

public class ConfiguracionExc extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5808785040506109389L;

	public ConfiguracionExc() {
		super();
	}
	
	public ConfiguracionExc(String message) {
		super(message);
	}
	
	public ConfiguracionExc(String message, Throwable cause) {
		super(message, cause);
	}
	
	public ConfiguracionExc(Throwable cause) {
		super(cause);
	}
	
}
